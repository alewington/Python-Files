# For Loop

# A for loop is used to iterate over a sequence (such as a list, tuple, or
# string) and execute a block of code for each item in the sequence.

# The general syntax of a for loop is:
#   for <variable> in <sequence>:
#       <block of code>

# In this example, we iterate over the list [10, 20, 30] and print each item
# to the console.
for item in [10, 20, 30]:
    print(item)

# The for loop will execute the block of code once for each item in the
# sequence.

# Output:
# 10
# 20
# 30
