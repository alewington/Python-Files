# If you want to execute a block of code when the condition is false, use an
# else statement.

# Syntax:
#   if <condition>:
#       <block of code>
#   else:
#       <block of code>

# Example:
x: int = -5
if x > 0:
    print("x is positive")
else:
    print("x is not positive")

# The above example will print "x is not positive" because the condition
# (x > 0) is false.
