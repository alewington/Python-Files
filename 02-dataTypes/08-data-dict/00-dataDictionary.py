# Data Dictionary

# What is a python dictionary? 
# A python dictionary is an unordered collection of key-value pairs, where the keys are unique. 

# What is a key in a dictionary?
# A key is a label that you can use to refer to a value in a dictionary. 

# What is a value in a dictionary?
# A value is the data associated with a key in a dictionary. 

# An example would be 
# { "key": "value", "key2": "value2" }

# How do I create an empty dictionary?
# We can use the built-in function dict() to create an empty dictionary.
# >>> empty_dict = dict()

# How do I add a key and value pair to my dictionary?
# To add a key and value pair to your dictionary, we first need to create the dictionary. 
# Then, we can use square brackets [] to access the value associated with a particular key in our dictionary. 
# For example: empty_dict["key"] = "value"

