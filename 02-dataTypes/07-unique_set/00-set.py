# Set

# A set is a collection of unique elements, also known as members
# or values, that can be unordered and unindexed. In other words,
# sets have no particular order or position for their members,
# and each member can only appear once in the set. Sets are often
# used to represent relationships between objects, such as "all the
# students in this class" or "all the elements in this set".

# Sets are typically denoted by curly braces {} and are used with
# mathematical notation such as union, intersection, and difference.
# They can also be used in programming languages like Python to create
# lists of unique items.

# Remember:
# sets are unordered collections of unique elements
# Sets are defined by { element1, element2, ... } syntax
# Duplicate elements in the set definition will be ignored
# Sets can contain mixed data types (integers, strings, etc.)
# Sets cannot contain mutable objects like lists or dictionaries

my_set: set[int] = {1, 2, 3, 4, 5}
print(my_set)
