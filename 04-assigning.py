# Assigning multiples
# Many to Many

a, b, c = 1, 2, 3
print(a)
print(b)
print(c)

# One to Many
a = b = c = 1
print(a)
print(b)
print(c)

# expand a list into multiple variables
apples = ["apple", "golden apple", "enchanted golden apple"]
apple1, apple2, apple3 = apples
print(apple1)
print(apple2)
print(apple3)
