# Copy Lists

# 1. Create a list of names
minecraft_npc = ["Creeper", "Skeleton", "Spider"]

# 2. Print the list
print(minecraft_npc)

# 3. Make a copy of the list
minecraft_npc_copy = minecraft_npc[:]

# 4. Print the original and copied lists
print(f'original{minecraft_npc}')
print(f'copy {minecraft_npc_copy}')
