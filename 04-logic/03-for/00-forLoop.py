# For Loop

# When to use for Loop?

# - when you want to repeat something a number of times
# - when you know how many times it will be repeated ( may need to
# be calculated)
# - when you want to do something with each element in an array or list (no
# condition)
# -Iterate over a list


# Don't

# If you don't know how many times to loop, use a while loop
# If you don't know when to stop, use a while loop <EOT>
# If you're transforming data into a new list think about using list
# comprehensions.


# Quick Guide

# Use for when you know the number of loops
# Use while when you know when to stop, not how many times


# Syntax:

# for <variable> in <iterable>:
#   <statement(s)>

# Example 1:
for i in range(5):
    print("Hello World!")

# Output:
# Hello World!
# Hello World!
# Hello World!
# Hello World!
# Hello World!
