# Subtraction Operator

# The subtraction operator is - . It subtracts the second number from
# the first.
# For example, 5 - 2 is 3.
print(5 - 2)  # = 3

# Another example 10 - 4 is 6.
print(10 - 4)  # = 6

# The subtraction operator can be used with more than two numbers.
# For example, 10 - 4 - 2 is 6.
print(10 - 4 - 2)  # = 6

# You can also use the subtraction operator to subtract a number from itself.
# For example, 5 - 5 is 0.
print(5 - 5)  # = 0

# with the use of brackets 10 - (4 - 2) is 8.
print(10 - (4 - 2))  # = 8

# The subtraction operator can be used to subtract a negative number from
# another number. For example, 5 - (-3) is 8.
print(5 - (-3))  # = 8
