# Division Operator

# The divisions operator is / and it returns the quotient of two numbers.

print(2 / 4)  # 0.5

# other examples:

print(3 / 6)  # 0.5

print(1 / 3)  # 0.3333333333333333

print(0.3/3)  # 0.1

# other uses for the division oeperator are:

# 1. to calculate the average(mean) of a list of numbers

numbers: list[int] = [2, 4, 6]
average: float = sum(numbers) / len(numbers)
print(average)  # 4

# 2. to calculate the percentage of one number over another:

percentage: float = 100 * (3 / 5)
print(percentage)  # 60.0
