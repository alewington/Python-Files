# Python Operator Precedence

# BEDMAS, BODMAS, PEMDAS etc

# Brackets, Operators, Division/Multiplication, Addition/Subtraction
# are the order of operations. Highest at the top.

# Python uses the following order of operations:
#   Operator	            Description
#   ()	                    Brackets
#   **      	            Exponentiation
#   +x  -x  ~x	            Unary plus, unary minus, and bitwise NOT
#   *  /  //  %	            Multiplication, division, floor division & modulus
#   +  -	                Addition and subtraction
#   <<  >>	                Bitwise left and right shifts
#   &	                    Bitwise AND
#   ^	                    Bitwise XOR
#   |	                    Bitwise OR
# == !=  > # >=  <  <=      Comparisons
# is  is not                Identity, non-identity
# in  not in 	            Membership operators
#   not	                    Logical NOT
#   and	                    AND
#   or	                    OR
