# Conditional Statements
# ======================

# If Statement
# -------------

# The if statement is used to execute a block of code when a specified
# condition is true. If the condition is false, the block of
# code is skipped.

# Syntax:
#   if <condition>:
#       <block of code>

# Example:
x: int = 5
if x > 0:
    print("x is positive")

# The above example will print "x is positive" because the condition (x > 0)
# is true.

# If you want to execute a block of code when the condition is false, use an
# else statement.
