# Logic in python also known as Control Flow

# Logic in Python refers to the use of control flow statements to determine
# the order in which code is executed. Control flow statements allow you to
# make decisions, repeat actions, and control the flow of your program
# based on certain conditions. The main types of control flow statements
# in Python are:

# 1. Conditional Statements: These statements allow you to execute different
# blocks of code based on certain conditions. The most common conditional
# statements are if, elif, and else.

# 2. Looping Statements: These statements allow you to repeat a block of code
# multiple times. The most common looping statements are for and while loops.

# 3. Control Flow Keywords: These keywords allow you to control the flow of
# your program in more advanced ways. The most common control flow keywords are
# break, continue, and pass.
