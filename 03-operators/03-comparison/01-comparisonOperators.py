# Comparison Operators

# Comparison Operators are used to compare two values and return a
# boolean value.

# The Comparison Operators are:

# == Equals to
# == is the equals 2 operator, it returns true if both operands are equal.
# 10 == 10 # True
print(10 == 10)  # True
# 10 == 9 # False
print(10 == 9)  # False

# != Not Equals to
# The not equals operator returns true if both operands are not equal.
# 10 != 10 # False
print(10 != 10)  # False
# 10 != 9 # True
print(10 != 9)  # True

# > Greater than
# The greater than operator returns true if the left operand is greater than
# the right operand.
# 10 > 9 # True
print(10 > 9)  # True
# 10 > 10 # False
print(10 > 10)  # False

# < Less than
# The less than operator returns true if the left operand is less than
# the right operand.
# 10 < 9 # False
print(10 < 9)  # False
# 10 < 10 # False
print(10 < 10)  # False

# >= Greater than or Equals to
# The greater than or equals operator returns true if the left operand is
# greater than or equal to the right operand.
# 10 >= 9 # True
print(10 >= 9)  # True
# 10 >= 10 # True
print(10 >= 10)  # True

# <= Less than or Equals to
# The less than or equals operator returns true if the left operand is
# less than or equal to the right operand.
# 10 <= 9 # False
print(10 <= 9)  # False <EOT>
# 10 <= 10 # True
print(10 <= 10)  # True
