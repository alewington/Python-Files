# Clear a set

# Clearing a set is useful when you want to reuse the set variable but
# don't have any data left to store in it.
# The clear() method removes all items from an existing set. It doesn't
# return any value.

my_set = {1, 2, 3, 4, 5}
print(my_set)

# Clearing a set
my_set.clear()
print(my_set)
