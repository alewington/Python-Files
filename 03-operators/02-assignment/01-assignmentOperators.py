# Assignment operators

# Assignment Operators are used to assign values to variables.
# Python offers several assignment operators, listed below.
# 	Operator	Name                        Example
#   =	        assign                      x = 5 <EOT>
#   +=          Addition assignment         x += 5 is equivalent to x = x + 5
#   -=          Subtraction assignment      x -= 5 is equivalent to x = x - 5
#   *=          Multiplication assignment   x *= 5 is equivalent to x = x * 5
#   /=          Division assignment         x /= 5 is equivalent to x = x / 5
#   %=          Modulus assignment          x %= 5 is equivalent to x = x % 5
#   //=         Floor division assignment   x //= 5 is equivalent to x = x // 5
#   **=         Exponentiation assignment   x **= 5 is equivalent to x = x ** 5
#   &=          Bitwise AND assignment      x &= 5 is equivalent to x = x & 5
#   |=          Bitwise OR assignment       x |= 5 is equivalent to x = x | 5
#   ^=          Bitwise XOR assignment      x ^= 5 is equivalent to x = x ^ 5
#   >>=         Right shift assignment      x >>= 5 is equivalent to x = x >> 5
#   <<=         Left shift assignment       x <<= 5 is equivalent to x = x << 5


# Examples for each assignment:
# Assignment Operators
# Useful for assigning values to variables.
x = 10
print(x)

# Addition Assignment
# useful for incrementing or decrementing a variable.
x += 2
print(x)

# Subtraction Assignment
# Useful for decrementing a variable.
x -= 3
print(x)

# Multiplication Assignment
# Useful for multiplying a variable.
x *= 4
print(x)

# Division Assignment
# Useful for dividing a variable.
y = 50
y /= 5
print(y)

# Modulus Assignment
# Useful for getting the remainder of a variable.
z = 60
z //= 6
print(z)


# Exponentiation Assignment
# Useful for raising a variable to the power of another.
x **= 7
print(x)

# Bitwise AND Assignment
# Useful for getting the bitwise AND of two variables.
a = 8
a &= 8
