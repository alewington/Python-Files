# Example:

count: int = 0
while count < 5:
    print(f"Count: {count}")
    count += 1
# In this example, the while loop will continue to execute as long as the
# condition (count < 5) is true. The loop will print the current value of
# count and increment it by 1 on each iteration. Once count reaches 5, the
# condition will become false, and the loop will terminate.
